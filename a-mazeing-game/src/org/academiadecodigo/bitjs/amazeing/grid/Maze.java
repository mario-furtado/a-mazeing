    public void paintMaze(SimpleGfxGrid grid) {
        for (int row = 0; row < scheme.length; row++) {        // maze.length retorna o numero de rows (8)
            for (int col = 0; col < scheme[0].length; col++) {  // maze[0].length retorna o numero de cols (11)

                switch (scheme[row][col]) {
                    case 0:
                        fixedBush = new FixedBush(row,col,grid);
                        fixedBush.init();
                        break;
                    case 1:
                        path = new Path(row,col,grid);
                        path.init();
                        break;
                    case 2:
                        upMiddleFence = new UpMiddleFence(row,col,grid);
                        upMiddleFence.init();
                        break;
                    case 3:
                        walkableBush = new WalkableBush(row,col,grid);
                        walkableBush.init();
                        break;
                    case 4:
                        path = new Path(row,col,grid);
                        path.init();
                        break;
                    case 5:
                        path = new Path(row,col,grid);
                        path.init();
                        break;
                    case 6:
                        leftFence = new LeftFence(row,col, grid);
                        leftFence.init();
                        break;
                    case 7:
                        rightFence = new RightFence(row,col, grid);
                        rightFence.init();
                        break;
                    case 8:
                        downMiddleFence = new DownMiddleFence(row,col,grid);
                        downMiddleFence.init();
                        break;
                    case 9:
                        trophy = new Trophy(row,col,grid);
                        trophy.init();
                        break;
                    case 10:
                        leftCorner = new LeftCorner(row,col,grid);
                        leftCorner.init();
                        break;
                    case 11:
                        rightCorner = new RightCorner(row,col,grid);
                        rightCorner.init();
                        break;
                    case 12:
                        nonPath = new NonPath(row,col,grid);
                        nonPath.init();
                        break;
                }
            }
        }
    }

